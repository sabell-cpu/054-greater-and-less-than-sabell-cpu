function oldEnough(age) {
    if (age > 17) {
        return "old enough";
    }
}

function underAge(age) {
    if (age < 18) {
        return "under age";
    }
}

function legalSpeed(speed) {
    if (speed <= 50) {
        return "legal speed";
    }
}

function isGoldenYears(age) {
    if (age >= 65) {
        return "golden years";
    }
}
